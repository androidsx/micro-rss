package com.androidsx.microrss.view.extra;

/** Names of the extras that are passed around between the different views. */
public class ExtrasConstants {
    
    /** Sorted integer array with the IDs of all stories in a feed. */
    public static final String STORY_IDS = "story-ids";
    
    /** Index of the story that the view is expected to display. */
    public static final String STORY_INDEX = "story-index";

    /** Sorted integer array with the IDs of all feeds. */
    public static final String FEED_IDS = "feed-ids";
    
    /** Index of the current feed. */
    public static final String FEED_INDEX = "feed-index";
}
